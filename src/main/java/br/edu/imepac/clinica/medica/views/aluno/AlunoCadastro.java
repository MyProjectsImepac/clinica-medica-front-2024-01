    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (validar_campos_formulario()) {
            return;
        }
        Aluno aluno = criar_entidade_aluno();
        salvar_aluno_database(aluno);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void salvar_aluno_database(Aluno aluno) {
        try {
            AlunoDao alunoDao = new AlunoDao();
            boolean resposta = alunoDao.save(aluno);

            if (resposta == true) {
                alunoDao.close();

                JOptionPane.showMessageDialog(this,
                        "Cadastro realizado com sucesso!");
                nome.setText("");
                matricula.setText("");
            } else {
                JOptionPane.showMessageDialog(this,
                        "Não foi possível efetuar o cadastro!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AlunoCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private Aluno criar_entidade_aluno() {
        Aluno aluno = new Aluno(nome.getText(), matricula.getText());
        return aluno;
    }

    private boolean validar_campos_formulario() throws HeadlessException {
        //VALIDAR CAMPOS
        if (nome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Campo nome obrigatório!");
            return true;
        }
        return false;
    }

